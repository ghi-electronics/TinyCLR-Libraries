namespace System.Xml
{
  internal class XmlNamespace
  {
    public readonly string Prefix;
    public readonly string NamespaceURI;

    public XmlNamespace(string prefix, string namespaceURI)
    {
      this.Prefix = prefix;
      this.NamespaceURI = namespaceURI;
    }
  }
}
